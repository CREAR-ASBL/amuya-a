    private void ñb_ConsOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ñb_ConsOkActionPerformed
        // Si alguna conjunción seleccionada en ñl_Cons pertenece a la
        // disyunción seleccionada en ñl_Ram, informar del error y regresar
        for(int i : ñl_Cons.getSelectedIndices()){
            if (i/3 == GUI.ñl_Ram.getSelectedIndex()){
                JOptionPane.showMessageDialog(rootPane, "La(s) conjuncion(es) "
                        + "seleccionada(s) pertenece(n) a la misma disyunción, \n"
                        + "pero una disyunción no puede descender de si misma.",
                        "Error de selección",
                        JOptionPane.INFORMATION_MESSAGE);
//                allOk=false;
                return;
            }
        }

        // Ahora establecer los padres de la disyunción.

        Ñ.table.setParents(Ñ.table.getDisjunctions().
                get(GUI.ñl_Ram.getSelectedIndex()),
                ñl_Cons.getSelectedIndices());

        super.dispose();

        // recargar la lista de padres en ñl_RamFrom
        GUI.listModelRamParents.clear();
                for(String s : Ñ.table.getAllParentsNames(Ñ.table.getDisjunctions().get(GUI.ñl_Ram.getSelectedIndex()))){
                    GUI.listModelRamParents.addElement(s);
                }
        
    }//GEN-LAST:event_ñb_ConsOkActionPerformed

